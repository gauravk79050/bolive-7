          <h4 class="post-title"><span class="post-meta"><u>5 GOUDEN TIPS om uw klanten online te laten bestellen (en uzelf tijd te besparen)</u></h4>
          <p>Krijgt u naar uw mening te weinig bestellingen binnen via de webwinkel,   dan kunnen de volgende tips zeker voor u interessant zijn.<br />
            <br />
            Buiten de externe factoren die mee het succes van het online   bestellen bepalen (ligging, concurrentie, sector,..) zijn er nog een   aantal andere zaken die hier invloed hebben. Het doel van de webwinkel   is vooral om u tijd en zo geld te besparen. <br />
            <br />
            Hieronder de <u><strong>5 gouden tips</strong></u> om de webwinkel optimaal te benutten en zoveel mogelijk verkeer naar uw site te leiden.
           
          </p>
          <ul>
            <li><strong>Bestellingen per tel/mail/fax</strong><br />
              ...zijn zeker de zaken die u moet vermijden. U verliest er veel tijd mee, u beschikt niet over de klantengegevens (buiten het <a href="http://tel.nr" target="_blank">tel.nr</a> - die dan hopelijk werkt) en u heeft niet zwart op wit wat juist de   bestelling omvat. Probeer daarom discreet op het einde van elk tel.   gesprek te vermelden dat de bestellingen ook online kunnen doorgegeven   worden (en dat ze daar extra kortingen kunnen krijgen).<br />
              <br />
            </li>
            <li><strong>TV-scherm</strong><br />
              Indien u een webwinkel heeft maar er   totaal geen reclame voor maakt zullen uw klanten dit ook niet te weten   komen. De beste reclame in de winkel wordt gevoerd via een TV-scherm die   ergens in de winkel staat waar er promoties de revue passeren die uw   klanten enkel via de webwinkel kunnen bedingen. Men mag zeker niet   onderschatten wat voor een invloed dit heeft op het koopgedrag van de   klanten (!)... Tijdens het wachten kijkens ze naar alles wat beweegt :)<br />
              <br />
            </li>
            <li><strong>Flyers</strong><br />
              Buiten de mond-aan-mond reclame zijn   flyers ook belangrijk - steek een flyer in elke zak en wanneer de klant   deze flyer een paar keer in de zak heeft aangetroffen is de kans groot   dat hij/zij toch even een kijkje gaat nemen op de webwinkel.<br />
              <br />
            </li>
            <li><strong>Foto's</strong><br />
              Maak de webwinkel attractief adhv foto's -   een webwinkel met enkel tekst komt saai over en nodigt niet direct uit   om er naar terug te gaan<br />
              <br />
            </li>
            <li><strong>Bestellingen opvolgen</strong><br />
              ... klinkt misschien grappig, maar af en toe wordt er vergeten om een bestelling klaar te zetten door een vergetelheid of   doordat de bestelling gemist wordt (met alle gevolgen van dien). Heeft u   dit reeds voor gehad kan misschien onze Mail2print addon voor u een   oplossing betekenen. Hierbij wordt een bestelling automatisch afgeprint   uit een gewone printer en hoeft u niet steeds uw controlepaneel in het   oog te houden. De klant krijgt dan 1 mail waarin vermeld wordt dat de   bestelling op het afgesproken tijdstip klaar staat. Een bestelling voor volgende week? Dan wordt de dag dat de klant de bestelling automatisch een overzicht s'morgens afgeprint, handig toch?  <br />
            </li>
          </ul>
