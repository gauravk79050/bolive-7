          <div class="titlepost"><br>
            <p>Versie 2.4 omvat enkele tussentijdse updates (2.3.2 tem 2.3.5)<br />
            </p>
            <ul>
              <li><strong>Timeslot</strong>: Herkent u het probleem dat mensen allemaal op hetzelfde tijdstip bestellingen komen afhalen? Hiervoor hebben we een speciaal timeslot ingebouwd zodat uw klanten om de X aantal minuten de bestelling kunnen afhalen. Voorbeeld: komt klant X om 12 u een bestelling afhalen kan klant Y 12 uur niet meer selecteren.</li>
              <li><strong>Geavanceerde afhaal/leverinstellingen</strong>: Indien op een verlofdag of feestdag een bestelling werd doorgevoerd, kon dit voor de volgende dag problemen geven... dit is vanaf nu geschiedenis: u kan <br />
                nu zelf per dag bepalen wanneer de klant een bestelling kan afhalen (ook op feestdagen!).</li>
              <li><strong>Nieuwe kalender functie: </strong>Wij plaatsen voor u reeds alle feestdagen in de kalender zodat u enkel nog uw verlofperiode moet ingeven (wederom tijdswinst en kans op misverstanden verkleint hiermee)</li>
              <li><strong>ADDONS</strong>: Vanaf nu heeft u de mogelijkheid om extra functies te activeren via uw controlepaneel. <br />
Automatisch afprinten van een bestelling, een DYMO labeler-functie en een klantenkorting module zijn enkele addons die werden toegevoegd toegevoegd (check: )</li>
              <li><strong>Vraag en antwoord</strong>: U kan nu uw persoonlijke &quot;Vraag &amp; Antwoord&quot; plaatsen op website. Indien uw klanten een vraag hebben kunnen ze het antwoord vinden via een link onderaan de pagina (webwinkel).</li>
              <li><strong>Uploaden foto door klant</strong>: Heeft u een bepaald artikel waarbij de klant een foto kan uploaden (fototaart) dan kan u per product activeren.</li>
              <li><strong>Verschillende talen</strong>: Vanaf nu kan u beslissen in welke taal of talen u de webwinkel wil laten zien (nl-fr-eng)</li>
            </ul>
