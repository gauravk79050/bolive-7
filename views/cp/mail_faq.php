        <p><span class="titlepost"><span class="posttitle">Changelog versie 2.5</span></span><span class="titlepost"> (released 18-08-2013) - <span class="red">** NIEUW **</span></span></p>
                        <!--POST -->

        <div class="post">

          <div class="titlepost"><br />
            
            <ul>
              <li><strong>Facebook-login</strong> (nog)<br />
                Wanneer uw klanten over een   FB-account beschikken kunnen ze deze gebruiken om te registreren op de   webwinkel, maw hoeven ze geen nieuwe account aan te maken via de   webwinkel (wat toch voor vele klanten een drempel is). <br />
                Leuk detail: wanneer iemand in zijn FB-profiel geen adresgegevens of   telefoonnr. heeft ingegeven gaat het systeem automatisch vragen om dit   aan te vullen (enkele in onze database). Op die manier beschikt u steeds   over de volledige info en kan u de klant contacteren indien er zich een   probleem voordoet.<br />
                <br />
              </li>
              <li><strong>IE6-7 fix</strong><br />
                Het is geweten dat klanten problemen   ondervinden tijdens het winkelen wanneer ze een een oudere browser   gebruiken. Vanaf nu krijgen deze klanten een bericht dat ze eerst de   browser moeten updaten alvorens ze kunnen verder gaan (in hun eigen   belang).<br />
                <br />
              </li>
              <li><strong>Nieuwe leveringsinstellingen</strong><br />
                We hebben de   leveringsinstellingen drastisch hervormd. Het is nu mogelijk om de klant   te laten betalen per km (wordt automatisch berekend via google maps) of   om een vast bedrag te bepalen per regio. <br />
                <br />
              </li>
              <li><strong>Levering</strong><br />
                Bij levering hebben we een checkbox   &quot;kopieer adresgegevens&quot; toegevoegd, zodat de velden automatisch worden   ingevuld met dezelfde gegevens als wat ze hebben ingegeven tijdens het   registreren (tijdswinst).<br />
                <br />
              </li>
              <li><strong>Registreren</strong><br />
                Klant krijgt nu ook een automatische mail waarin de logingegevens staan (kunnen ze dan steeds opzoeken)<br />
                <br />
              </li>
              <li><strong>Paypal</strong><br />
                Men kan een minimum bedrag bepalen alvorens men via Paypal kan betalen.<br />
                <br />
              </li>
              <li><strong>Paging</strong><br />
                U kent het wel...indien u binnen een   bepaalde categorie zeer veel producten heeft staan dan wordt de pagina   zeer lang en moeten de mensen serieus beginnen scrollen. Wanneer deze   pagina te lang wordt hij vanaf nu automatisch onderverdeelt in   verschillende pagina's wat gemakkelijker navigeerbaar is.<br />
                <strong><br />
                </strong></li>
              <li><strong>Zoekfunctie toegevoegd</strong><br />
                Uw klanten kunnen nu zoeken naar een bepaald product - zeer handige tool!<br />
                <br />
              </li>
              <li><strong>Suggesties</strong><br />
                Wanneer   de klant wil afrekenen ziet hij nog enkele suggesties. Deze functie kan   u zelf (gratis) activeren en u kan zelf bepalen welke suggesties u wil   tonen.<br />
                <br />
              </li>
              <li><strong>Producten per gewicht</strong><br />
                Klant kan nu zelf bepalen of hij gram of kg neemt<strong><br />
                </strong>              </li>
            </ul>
            <ul>
              <li><strong>Afbeeldingen uploaden<br />
                </strong>..   gaat nu veel gemakkelijker en sneller dan voorheen. Men hoeft niet   eerst de afbeeldingen te verkleinen of bij te snijden, dit alles wordt   achter de schermen automatisch voor u gedaan.<br />
                <br />
              </li>
              <li><strong>API codes</strong><br />
                In de profiel pagina kan u gemakkelijk de API-codes doorsturen naar uw webmaster (indien nodig)<br />
                <br />
              </li>
              <li><strong>Verberg tarieven</strong><br />
                Het   is vanaf nu mogelijk om de tarieven te verbergen tot ze (de klanten)   zijn ingelogd. Enkel uw bestaande klanten zien dus uw tarieven.<br />
                <br />
              </li>
              <li><strong>Termijn product wijzigen</strong><br />
                Stel dat u heeft   ingesteld: &quot;bestel vandaag voor 11u kan u het product afhalen de   volgende dag&quot;... maar u heeft een product X dat meer tijd vergt (en dat   dus niet de volgende dag kan afgehaald worden), dan kan u gemakkelijk   extra aantal dagen opgeven per product zodat product X niet de volgende   dag maar +2 dagen later kan worden afgehaald.<br />
                <br />
              </li>
              <li><strong>Minimum tarief voor levering</strong><br />
                Men moet minstens voor X&euro; bestellen alvorens de producten geleverd kan worden (kan zelf ingesteld worden).<br />
                <br />
              </li>
              <li><strong>Rapporten</strong><br />
                Het   was al mogelijk om een overzicht van de bestelde producten af te   printen voor een bepaalde dag/periode. Vanaf nu is het mogelijk om een   overzicht af te printen van de bestellingen zelf (met en zonder factuur)   - handig voor de boekhouding!<br />
                <br />
              </li>
              <li><strong>Ingredi&euml;nten (!)</strong><br />
                Vanaf 2014 zal het verplicht worden om de ingredi&euml;nten te vermelden per product (<a href="http://www.allergenenconsultancy.nl/in/wetgeving" target="_blank">http://www.allergenenconsultancy.nl/in/wetgeving</a> ). Belangrijk om weten is dat wij reeds serieuze contacten hebben   gelegd (tal van leveranciers) om alsook met ons systeem in regel te   zijn. Naar de toekomst toe zal het met ons systeem mogelijk zijn om   gemakkelijk de ingredi&euml;nten van een bepaalde leverancier (Heinz, De Vos,   etc..) te selecteren en toe te wijzen aan een bepaald product.   Voorlopig hebben we een apart veld toegevoegd in de product detailpagina   waar u nu reeds de ingredi&euml;nten zelf kan invullen. Deze ingredi&euml;nten   worden vertoond in de popup (in de webwinkel).</li>
            </ul>
          </div>
